RHYME_MAP = build_rhyme_map()


def get_final(char: str) -> str:
    """获取汉字的韵母（归一化后）"""
    try:
        py = pinyin(char, style=Style.FINALS, strict=False)
        raw = py[0][0] if py and py[0] else ""
        return _normalize_final(raw)
    except Exception:
        return ""


def get_rhyming_chars(target_final: str, top_n: int = 80) -> list[str]:
    """获取与目标韵母押韵的汉字列表"""
    if target_final in RHYME_MAP:
        return RHYME_MAP[target_final][:top_n]
    # 尝试模糊匹配
    for group, chars in RHYME_MAP.items():
        if target_final.startswith(group) or group.startswith(target_final):
            return chars[:top_n]
    return []


def _is_rhyme_match(f1: str, f2: str) -> bool:
    """宽松押韵匹配"""
    if not f1 or not f2:
        return False
    return f1 == f2  # 已归一化，直接比较
