# ── 韵母归一化：合并近韵 ──
# 「十八韵」简化版 — 中华新韵
RHYME_GROUPS = {
    "a":  ["a", "ia", "ua"],
    "e":  ["e", "o", "uo"],
    "ie": ["ie", "üe", "ue"],
    "i":  ["i", "er", "v"],          # v = ü 的 ASCII 替代
    "u":  ["u"],
    "ü":  ["ü", "v"],
    "ai": ["ai", "uai"],
    "ei": ["ei", "ui", "uei"],
    "ao": ["ao", "iao"],
    "ou": ["ou", "iu", "iou"],
    "an": ["an", "ian", "uan", "üan"],
    "en": ["en", "in", "un", "ün"],
    "ang": ["ang", "iang", "uang"],
    "eng": ["eng", "ing", "ong", "iong"],
}


def _normalize_final(final: str) -> str:
    """韵母归一化到押韵组"""
    if not final:
        return ""
    # 去掉声母只保留韵母核心，然后映射
    for group, members in RHYME_GROUPS.items():
        if final in members:
            return group
    # 模糊匹配：去掉介母 i/u/ü 后再试
    core = re.sub(r"^[iuüv]", "", final)
    for group, members in RHYME_GROUPS.items():
        for m in members:
            if core == re.sub(r"^[iuüv]", "", m):
                return group
    return final


